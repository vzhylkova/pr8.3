<?php
// Перевіряємо, чи були передані змінні a і b через POST
if (isset($_POST['a']) && isset($_POST['b'])) {
    // Отримуємо значення змінних a і b з форми і змінюємо їх тип на цілі числа
    $a = (int)$_POST['a'];
    $b = (int)$_POST['b'];

    // Виконуємо математичні операції
    $sum = $a + $b;
    $product = $a * $b;
    $difference = $a - $b;

    // Перевіряємо, чи друге число не є нулем, щоб уникнути ділення на нуль
    if ($b != 0) {
        $quotient = $a / $b;
    } else {
        $quotient = "Ділення на нуль";
    }

    // Виводимо результати
    echo "<h2>Результати математичних операцій:</h2>";
    echo "<p>Сума: $a + $b = $sum</p>";
    echo "<p>Добуток: $a * $b = $product</p>";
    echo "<p>Різниця: $a - $b = $difference</p>";
    echo "<p>Частка: $a / $b = $quotient</p>";
} else {
    // Якщо змінні a і b не були передані через POST, виводимо повідомлення про помилку
    echo "<h2>Помилка: Введіть значення для змінних a і b через форму.</h2>";
}
?>


